package interfaces.distintosInterfaces;

/**
 * Interfaz con varios métodos que deberán ser
 * implementados en la clas que implemente este interfaz
 * (si la clase implementa el interfaz pero no define algún método,
 * tendrá que declararse abstract)
 * @author magcarnota
 */
public interface InterfazVariosMetodos {
    public void saludaA(String saludado);
    public void despideteDe(String despedido);
}
